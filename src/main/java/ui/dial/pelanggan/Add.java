    private void kodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kodeKeyReleased
    if(!kode.getText().isEmpty())try {
        java.sql.PreparedStatement p=d.getPS("select (kode=?)as oke from pelanggan");
        p.setString(1, kode.getText());
        java.sql.ResultSet r=p.executeQuery();
        if(r.next()){
            if(r.getBoolean("oke"))kode.setForeground(Color.red);
            else kode.setForeground(Color.BLACK);
        }r.close();
        p.close();
    } catch (SQLException ex) {
        util.Db.hindar(ex);
    }refresh();
    }//GEN-LAST:event_kodeKeyReleased

