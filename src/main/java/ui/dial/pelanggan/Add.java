    private void refresh() {
        s.setEnabled(isAlmt()&&!kode.getText().isEmpty()&&Color.BLACK==kode.getForeground()&&!nm.getText().isEmpty()&&!telp.getText().isEmpty());
    }

    private boolean isAlmt() {
        boolean b=!desAlmt.getText().isEmpty();
        b|=!kabAlmt.getText().isEmpty();
        b|=!kecAlmt.getText().isEmpty();
        b|=!provAlmt.getText().isEmpty();
        return b;
    }

    private String getAlmt() {
        return desAlmt.getText()+", "+kecAlmt.getText()+", "+kabAlmt.getText()+", "+provAlmt.getText();
    }
